package com.example.basickotlin.operator

fun main() {
//    +
    println("tambah: ${1+1}")
//    -
    println("kurang: ${1-1}")
//    *
    println("kali: ${1*1}")
//    /
    println("bagi: ${1/1}")
//    %
    println("modulus: ${1%1}")
//    pangkat
    println("pangkat: ${Math.pow(2.0,3.0)}")
//    akar
    println("akar: ${Math.sqrt(36.0)}")
}